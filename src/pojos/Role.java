package pojos;

public class Role {
	
	
	private int level;
	private String description;
	
	
	public int getLevel() {
		return level;
	}
	public String getDescription() {
		return description;
	}
	public Role(int level, String description) {
		super();
		this.level = level;
		this.description = description;
	}
	
	
	
	

}
